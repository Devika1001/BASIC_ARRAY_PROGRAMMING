package com.firstprogram.abc;

public class ArraysDefaultProgram 
{

	public static void main(String[] args) 
	{
		int a1[]=new int[5];
		float a2[]=new float[5];
		double a3[]=new double[5];
		char a4[]=new char[5];
		boolean a5[]=new boolean[5];
		for(int i=0;i<=a1.length-1;++i)
		System.out.print(a1[i]+"\n");
		for(int i=0;i<=a2.length-1;++i)
			System.out.print(a2[i]+"\n");

		for(int i=0;i<=a3.length-1;++i)
			System.out.print(a3[i]+"\n");

		for(int i=0;i<=a4.length-1;++i)
			System.out.print(a4[i]+"\n");
		for(int i=0;i<=a5.length-1;++i)
			System.out.print(a5[i]+"\n");

	}

}
